export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState<Language>(languages[0]);

  const setLanguage = (language: Language) => {
    setCurrentLanguage(language);
    // ë¡œì»¬ ìŠ¤í† ë¦¬ì§€ì— ì €ì¥í•˜ì—¬ ìƒˆë¡œê³ ì¹¨ ì‹œì—ë„ ìœ ì§€
    localStorage.setItem('selectedLanguage', JSON.stringify(language));
  };

  // ë²ˆì—­ í•¨ìˆ˜
  const t = (key: string): string => {
    const langCode = currentLanguage.code as keyof typeof translations;
    return translations[langCode]?.[key] || key;
  };

  // ì»´í¬ë„ŒíŠ¸ ë§ˆìš´íŠ¸ ì‹œ ë¡œì»¬ ìŠ¤í† ë¦¬ì§€ì—ì„œ ì–¸ì–´ ì„¤ì • ë³µì›
  React.useEffect(() => {
    const savedLanguage = localStorage.getItem('selectedLanguage');
    if (savedLanguage) {
      try {
        const parsed = JSON.parse(savedLanguage);
        if (languages.find(lang => lang.code === parsed.code)) {
          setCurrentLanguage(parsed);
        }
      } catch (error) {
        console.error('Failed to parse saved language:', error);
      }
    }
  }, []);

  return (
    <LanguageContext.Provider value={{ currentLanguage, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

